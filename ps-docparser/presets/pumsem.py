"""
presets/pumsem.py — 건설 품셈 전용 프리셋 설정

Why: 건설 품셈 PDF는 "공통부문", "토목부문" 같은 특수 키워드가
     페이지 하단 푸터에 반복 등장하는 도메인 전용 패턴을 가진다.
     이를 범용 파이프라인에 하드코딩하면 일반 PDF에서 오동작한다.
     → 이 파일에서만 정의하고, --preset pumsem 시에만 로딩한다.

Phase 1 (기존): DIVISION_NAMES — 부문명 OR 패턴 (Phase 1 추출기용)
Phase 2 (신규): PARSE_PATTERNS — 파서 도메인 패턴 (Phase 2 파서용)
               TABLE_TYPE_KEYWORDS — 테이블 유형 분류 키워드 (Phase 2 파서용)
"""

import re

# ══════════════════════════════════════════════════════════
# Phase 1: 부문명 패턴 (기존 코드 유지)
# ══════════════════════════════════════════════════════════

# 건설 품셈 부문명 OR 패턴
# format_text_with_linebreaks()의 division_names 파라미터에 전달된다.
DIVISION_NAMES = (
    "공통부문|토목부문|건축부문|기계설비부문|"
    "전기부문|통신부문|조경부문|소방부문|"
    "기계부문|설비부문|전기설비부문"
)


def get_division_names() -> str:
    """품셈 프리셋의 부문명 패턴을 반환한다."""
    return DIVISION_NAMES


# ══════════════════════════════════════════════════════════
# Phase 2: 파서 도메인 패턴 (신규)
#
# Why: standalone_parser/config.py의 도메인 전용 패턴(8종)을 여기로 이동.
#      parsers/text_cleaner.py에 patterns 파라미터로 주입된다.
#      범용 모드(patterns=None)에서는 이 패턴들이 전혀 실행되지 않는다.
# ══════════════════════════════════════════════════════════

PARSE_PATTERNS = {
    # 주석 블록 감지
    "note_block_start": re.compile(r'^\[주\]\s*$', re.MULTILINE),
    "note_item": re.compile(
        r'([①②③④⑤⑥⑦⑧⑨⑩⑪⑫⑬⑭⑮])\s*(.*?)(?=(?:[①②③④⑤⑥⑦⑧⑨⑩⑪⑫⑬⑭⑮])|\Z)',
        re.DOTALL,
    ),
    # 할증/가산/감산 조건 (예: "경사 30% 이상인 경우 본 품의 20% 가산")
    "surcharge": re.compile(
        r'(.+?(?:경우|때|시))\s*(?:본\s*)?(?:품|시공량).*?(\d+)%.*?(가산|감산|감|증|할증)'
    ),
    # 교차참조 (예: "제6장 6-1-1 참조", "2-3-1 항 준용")
    "cross_ref": re.compile(
        r'(?:제(\d+)장\s+)?(\d+-\d+(?:-\d+)?)\s*(?:항?\s*)?(?:참조|준용|적용|따른다)'
    ),
    # 보완연도 (예: ('24년 보완), ('02, '22년 보완))
    "revision": re.compile(
        r"\('?(\d{2,4})(?:,\s*'?(\d{2,4}))*년\s*보완\)"
    ),
    # 단위 기준 (예: (일당), (m³당), (100m당))
    "unit_basis": re.compile(r'\(([^)]*당)\)'),
    # 장 제목 텍스트 — 정제 시 제거 대상 (예: "제6장 철근콘크리트공사")
    # Why: 품셈 문서에서 장 제목은 구조 마커 역할. 범용 문서에선 제거하면 안 됨.
    "chapter_title": re.compile(r'^(제\d+장\s+.+)$', re.MULTILINE),
    # 절/항 제목 (예: "6-1 콘크리트('25년 보완)")
    "section_title": re.compile(
        r'^(\d+-\d+(?:-\d+)?)\s+(.+?)$', re.MULTILINE
    ),
}


def get_parse_patterns() -> dict:
    """품셈 프리셋의 파서 도메인 패턴을 반환한다."""
    return PARSE_PATTERNS


# ══════════════════════════════════════════════════════════
# Phase 2: 테이블 유형 분류 키워드 (신규)
